        {
            //Autofac,Ninject,CastWindsor,StructureMap, LightInject,DryInject... bunlar dot.net projelerinde  kendi için ýos yapýsý yokken bu þekilde çalýþmak isteyenler için ortamlar sunuyor -->IoC Container
